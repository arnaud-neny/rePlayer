// Let Future Composer 1.4 initialization handle version 1.0 to 1.3, too:
bool HippelDecoder::SMOD_init(int songNumber) {
    traits.isSMOD = true;
    traits.periodMax = SMOD_PERIOD_MAX;
    return FC_init(songNumber);
}

