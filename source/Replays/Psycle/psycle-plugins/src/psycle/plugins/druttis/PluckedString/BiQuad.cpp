#include "BiQuad.h"
#include <cmath>
namespace psycle::plugins::druttis::plucked_string {
//============================================================================
//				Constructor
//============================================================================
BiQuad::BiQuad()
{
}
//============================================================================
//				Destructor
//============================================================================
BiQuad::~BiQuad()
{
}
//============================================================================
//				Init
//============================================================================
void BiQuad::Init()
{
	zeroCoeffs[0] = 0.0f;
	zeroCoeffs[1] = 0.0f;
	poleCoeffs[0] = 0.0f;
	poleCoeffs[1] = 0.0f;
	gain = 1.0f;
	Clear();
}
//============================================================================
//				Clear
//============================================================================
void BiQuad::Clear()
{
	inputs[0] = 0.0f;
	inputs[1] = 0.0f;
	lastOutput = 0.0f;
}
//============================================================================
//				SetRawCoeffs
//============================================================================
void BiQuad::SetRawCoeffs(float a1,float a2,float b1,float b2)
{
	poleCoeffs[0] = -a1;
	poleCoeffs[1] = -a2;
	zeroCoeffs[0] = b1;
	zeroCoeffs[1] = b2;
}
//============================================================================
//				SetGain
//============================================================================
void BiQuad::SetGain(float newGain)
{
	gain = newGain;
}
}